
package com.baz.scc.turnadorweb.model;

public class CjCRTurnosAtendidos {
    private Integer turnos;
    private Integer turnosVirtuales;
    private Integer total;

    public Integer getTurnos() {
        return turnos;
    }

    public void setTurnos(Integer turnos) {
        this.turnos = turnos;
    }

    public Integer getTurnosVirtuales() {
        return turnosVirtuales;
    }

    public void setTurnosVirtuales(Integer turnosVirtuales) {
        this.turnosVirtuales = turnosVirtuales;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

}
